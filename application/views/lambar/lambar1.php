            Gunung Seminung <br><br>

Gunung Seminung berada di barat laut Kota Liwa, ibukota Lampung Barat dengan jarak sekitar 25 kilometer. Gunung ini terletak di perbatasan antar propinsi, beberapa wilayahnya menjadi kecamatan Sukau, kabupaten Lampung Barat; sementara yang lain berada di Ranau, di provinsi Sumatera Selatan. Di kaki gunung, traveler dapat menemukan danau terbesar kedua di Pulau Sumatera, Danau Ranau. <br> <br>

Gunung Seminung memiliki ketinggian sekitar 1.881 meter di atas permukaan laut. Bahkan tingginya tidak terlalu tinggi, tetapi memiliki pemandangan yang bagus dari puncak gunung. Selain bunga edelweis yang dapat ditemukan di puncaknya, pemandangan danau yang membentang di atas Gunung akan memikat Anda <br> <br>

Umumnya, untuk mencapai puncak Seminung dapat ditempuh sekitar dua jam pendakian. Dari lokasi pemandian air panas, dekat ke Gunung, Anda dapat berjalan ke kanan di sepanjang tepi danau dan kemudian mulai memasuki jalan yang akan terus ke puncak. Tebingnya cukup curam dan dilalui dengan melintasi perkebunan kopi yang dimiliki oleh penduduk setempat. Di beberapa lokasi Anda akan melintasi jalur tanah yang telah dibuat oleh petani kopi dan ini akan memungkinkan Anda untuk melewatinya. Jika Anda beruntung, selama hikking Anda juga dapat mendengar suara binatang seperti owa dan kicau burung yang biasanya terdengar di pagi hari. <br> <br>

Setelah mendaki sekitar 90 menit, Anda akan tiba di lokasi di mana ada bangunan mushollah dan di sinilah Anda dapat menemukan sumber air terakhir. Terus berjalan sekitar 10 menit dari mushollah maka Anda akan tiba di hutan di ketinggian sekitar 800 meter di atas permukaan laut.Melalui pintu masuk hutan, vegetasi dimulai dengan keberadaan hutan tropis yang akan berlanjut sampai puncak. Sesampainya di puncak Gunung Seminung, Anda akan menemukan gubuk kecil beratap seng. Lokasi tidak begitu luas dan ada banyak pohon kering yang dihitamkan oleh kebakaran selama bertahun-tahun yang lalu. Rumput yang tinggi dengan segulung bunga edelweiss, menyajikan pemandangan yang luar biasa. <br> <br>

Fasilitas: <br> Untuk fasilitas secara resmi masih belum tersedia di kawasan Gunung Seminung, Namun jika anda membutuhkan angkutan atau rumah untuk menginap anda bisa menyewanya dari warga sekitar di kaki Gunung Seminung. Ada warung dan toko di sekitar rumah warga setempat yang menyediakan kebutuhan makanan dan minuman. Namun belum tersedia toko peralatan pendakian atau barang yang mendukung untuk aktifitas hiking.

