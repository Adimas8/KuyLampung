            Sejarah Museum Lampung<br><br>
            Museum Lampung mulai dibangun tahun 1975, peletakan batu pertama tahun 1978. Diresmikan oleh Menteri Pendidikan dan Kebudayaan 
            kala itu, Prof. Dr. Fuad Hasan, pada 24 September 1988. Bertepatan dengan peringatan Hari Aksara Internasional yang dipusatkan 
            di PKOR Way Halim.
            <br><br>
            Saat diberlakukan Otonomi Daerah, Museum Lampungmenjadi Unit Pelaksana Teknis Daerah (UPTD) di bawah Dinas Pendidikan. 
            Sejak tanggal 1 Januari 2008 kedudukan museum berubah menjadi UPTD Dinas Pariwisata (Parekraf). Dan di tahun 2016 lalu, 
            kembali menjadi UPTD Dinas Pendidikan. Mungkin karena ada pertimbangan lebih baik dengan pendekatan pendidikan ya untuk 
            pengelolaannya.
            <br><br>
            Melihat-lihat Koleksi Museum Lampung
            Saat kita memasuki halaman museum Lampung, kita sudah disuguhi beberapa koleksi. Ada meriam kuno peninggalan masa penjajahan 
            dan rumah panggung kayu berumur ratusan tahun yang dipindahkan dari desa Kenali Kecamatan Belalau Kabupaten Lampung Barat. 
            Kalau sempat ke Lampung Barat, coba juga lihat rumah lainnya yang mirip yang sama-sama berumur ratusan tahun.
            <br><br>
            Di bagian kiri taman, bisa kita lihat Jangkar, pelampung kapal, dan bola besi. Bola besi terbut biasa digunakan di daerah 
            tujuan transmigrasi pada 1953-1956. Digunakan untuk membuka lahan transmigrasi di wilayah Lampung Timur, Raman Utara dan 
            Purbolinggo, Kabupaten Lampung Timur, Seputih Banyak, dan Seputih Raman.
            <br><br>
            Di lantai satu bisa kita temui berbagai peninggalan prasejarah, benda-benda zaman Hindu – Budha, zaman kedatangan Islam, 
            masa penjajahan, dan paska kemerdekaan RI.
            Benda-benda pada masa kedatangan Islam antara lain: teko alpaka, talam, Prasasti Bohdalung berbahasa Jawa Banten, Alquran 
            tulis tangan di atas kertas deluang, stempel Marga Sabu. Serta naskah 15 halaman di kulit kayu aksara Lampung berbahasa Lampung, 
            Banten dan Arab.
            <br><br>
            Juga ada benda-benda peninggalan Radin Inten II. Serta beberapa prasasti seperti Prasasti Tanjung Raya II, Prasasti Dadak,
             Prasasti Ulu Belu, Prasasti Bawang, Prasasti Bungkuk, Prasasti Batu Bedil.
            Di Lantai dua, kita akan menemui koleksi aksesori dua sub-etnik Lampung, yaitu Saibatin dan Pepadun. Kedua sub-etnik ini 
            memiliki kekhasan masing-masing dalam ritual adat dan aksesorinya. Keduanya memiliki upacara daur hidup. Mulai dari upacara 
            kehamilan, kelahiran, masa kanak-kanak, masa remaja, masa dewasa, perkawinan dan kematian. Dalam upacara-upacara daur hidup 
            tersebut terkandung unsur falsafah bernilai luhur. Seperti nilai-nilai hubungan manusia dengan manusia, manusia dengan lingkungan, 
            dan manusia dengan supernatural.
            <br><br>
            Benda-benda Bernilai Sejarah<br><br>
            Koleksi museum Lampung itu harus mempunyai nilai sejarah. Tidak melulu soal usianya saja. Ada kriteria-kriteria yang harus 
            dipenuhi untuk bisa menjadi koleksi museum.
            Ada beberapa koleksi Museum Lampung yang merupakan hibah perorangan lho. Dan pihak museum khususnya serta Pemerintah Daerah 
            sangat mengapresiasi hal tersebut. Bukan dilihat dari rupiahnya, namun nilai-nilai yang terkandung dari koleksi tersebut.
            <br><br>
            Lokasi Museum Lampung<br><br>
            Museum Lampung sangat mudah dicari dan ditemukan. Perkiraan saya, hanya berjarak 300an meter dari terminal Rajabasa. 
            Tidak jauh dari lampu merah pertigaan masuk kampus Unila. Dari Rajabasa terletak di sebelah kiri jalan, dengan pelataran 
            parkir yang cukup luas.
            <br><br>
            Dari arah pusat kota Bandar Lampung, museum ini ada di sebelah kanan jalan H. Zainal Abidin Pagaralam. Berjarak sekitar 
            7,6 kilometer dari Tugu Adipura, dengan waktu tempuh sekitar 20 menit di tengah hari melewati 5-6 titik kemacetan.
